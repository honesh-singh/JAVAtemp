public class AssignmentOperatorsUse {
    public static void main(String[] args) {
        int b = 10;

        System.out.println("Initial value of b = " + b);
         
        b += 5; 
        System.out.println("b  = " + b);
        b -= 2;  
        System.out.println("b = " + b);
         b *= 3;  
        System.out.println("b = " + b);

        
        b /= 4;  
        System.out.println("b = " + b);

        b %= 3;  
        System.out.println("b  = " + b);
    }
}

